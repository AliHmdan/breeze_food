class FoodModel {
  final String restaurant;
  final String time;
  final double rating;
  final String orders;
  final String title;
  final String subtitle;
  final String price;
  final String image;

  FoodModel({
    required this.restaurant,
    required this.time,
    required this.rating,
    required this.orders,
    required this.title,
    required this.subtitle,
    required this.price,
    required this.image,
  });
}
